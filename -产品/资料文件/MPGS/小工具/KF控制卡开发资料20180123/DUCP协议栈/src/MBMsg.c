/*
    CRC16校验函数
*/
uint16_t MB_CRC16(uint8_t *pFrame, uint16_t count)
{
    uint8_t CRCHi = 0xFF;
    uint8_t CRCLo = 0xFF;
    int32_t index;
    while(count--) {
        index = CRCLo ^ *(pFrame++);
        CRCLo = (uint8_t)(CRCHi ^ _CRCHi[index]);
        CRCHi = _CRCLo[index];
    }
    return (uint16_t)(CRCHi << 8 | CRCLo );
}

/*
	协议栈数据输入
	Addr + FC + PN[2] + MSGID + DLEN + DATA[...] + CRC[2]
*/
int8_t MB_STK_In(uint8_t * pBuff,uint16_t Size)
{
	uint16_t i;
	uint8_t Data;
	
	if(_RxRdy) {
		return -1;
	}

	for(i = 0;i < Size;i ++) {
		Data = pBuff[i];
		if(_RxIndex == 0) {
			if((Data == _Addr) || (Data == BRODADDR0_DEF)) {
				_RxBuff[_RxIndex ++] = Data;
				_LstAddr = Data;
			}
		} else if(_RxIndex == 1) {
			if(Data == _FC) {
				_RxBuff[_RxIndex ++] = Data;
			} else {
				_RxIndex = 0;
			}
		} else if(_RxIndex < MB_PAK_BUFF_SIZE) {
			_RxBuff[_RxIndex ++] = Data;
			if(_RxIndex == 6) {
				if(Data > (Size - i - 1)) {
					Data = (Size - i - 1);
				}
				while(Data --) {
					_RxBuff[_RxIndex ++] = pBuff[++i];
				}
			} else if(_RxIndex > 6) {
				if(_RxIndex >= (_RxBuff[5] + 2 + 6)) {
					uint16_t CRC16;
					uint16_t CheckCode;
					CRC16 = MB_CRC16(&_RxBuff[0],_RxIndex - 2);
					CheckCode = _RxBuff[_RxIndex - 2];
					CheckCode |= _RxBuff[_RxIndex - 1] << 8;
					if(CRC16 == CheckCode) {
						_RxRdy = 1;
						if(_cbMsgRdy) {
							if(_cbMsgRdy((MB_MSG_T *) &_RxBuff[2]) == 0) {
								_RxRdy = 0;
							}
						}
					}
					_RxIndex = 0;
				} 
			}
		} else {
			_RxIndex = 0;
		}
	}
	return 1;
}

/*
	设置地址
*/
uint8_t MB_STK_SetAddr(uint8_t NewAddr)
{
	uint8_t Old;
	Old = _Addr;
	_Addr = NewAddr;
	_LstAddr = _Addr;
	return Old;
}

